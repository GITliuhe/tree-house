package com.zjt.entity;

public class parkingLot {
	private String parkingLotId;
	private String ownerId;
	private String pname;
	private String plocation;
	private String parkingSpaceQuatity;
	private String rates;
	public String getRates() {
		return rates;
	}
	public void setRates(String rates) {
		this.rates = rates;
	}
	public String getParkingLotId() {
		return parkingLotId;
	}
	public void setParkingLotId(String parkingLotId) {
		this.parkingLotId = parkingLotId;
	}
	public String getOwnerId() {
		return ownerId;
	}
	public void setOwnerId(String ownerId) {
		this.ownerId = ownerId;
	}
	
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public String getPlocation() {
		return plocation;
	}
	public void setPlocation(String plocation) {
		this.plocation = plocation;
	}
	public String getParkingSpaceQuatity() {
		return parkingSpaceQuatity;
	}
	public void setParkingSpaceQuatity(String parkingSpaceQuatity) {
		this.parkingSpaceQuatity = parkingSpaceQuatity;
	}
	
	

}
